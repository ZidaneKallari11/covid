             <h5>Pengertian</h5>
            Virus Corona atau severe acute respiratory syndrome coronavirus 2 (SARS-CoV-2) adalah
            virus yang menyerang sistem pernapasan. Penyakit karena infeksi virus ini disebut COVID-19.
            Virus Corona bisa menyebabkan gangguan ringan pada sistem pernapasan, infeksi paru-paru yang berat, hingga kematian.
            Severe acute respiratory syndrome coronavirus 2 (SARS-CoV-2) yang lebih dikenal dengan nama virus Corona
            adalah jenis baru dari coronavirus yang menular ke manusia. Walaupun lebih bayak menyerang lansia,
            virus ini sebenarnya bisa menyerang siapa saja, mulai dari bayi, anak-anak, hingga orang dewasa, termasuk ibu hamil dan ibu menyusui.<br>
<br>
            <h5>Gejala Virus Corona</h5>
            Gejala awal infeksi virus Corona atau COVID-19 bisa menyerupai gejala flu, yaitu demam, pilek, batuk kering, sakit tenggorokan, dan sakit kepala.
            Setelah itu, gejala dapat hilang dan sembuh atau malah memberat. Penderita dengan gejala yang berat bisa mengalami demam tinggi,
            batuk berdahak bahkan berdarah, sesak napas, dan nyeri dada. Gejala-gejala tersebut muncul ketika tubuh bereaksi melawan virus Corona.

